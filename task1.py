# Создаем класс Student (Студент), который будет представлять студента
class Student:
    # Определяем метод __init__, который вызывается при создании нового объекта класса
    # self - ссылка на текущий экземпляр класса
    # name - имя студента, student_id - идентификатор студента
    def __init__(self, name, student_id):
        # Присваиваем переданное имя атрибуту name объекта
        self.name = name
        # Присваиваем переданный идентификатор атрибуту student_id объекта
        self.student_id = student_id
        # Создаем пустой список grades (оценки) для хранения оценок студента
        self.grades = []
    
    # Метод для добавления оценки студенту
    # grade - оценка, которую нужно добавить
    def add_grade(self, grade):
        # Проверяем, что оценка находится в допустимом диапазоне (от 0 до 10)
        if 0 <= grade <= 10:
            # Если оценка допустима, добавляем ее в список grades
            self.grades.append(grade)
        else:
            # Если оценка недопустима, выводим сообщение об ошибке
            print("Ошибка! Оценка должна быть от 0 до 10")
    
    # Метод для вычисления среднего балла студента
    def get_average(self):
        # Если список оценок не пустой, возвращаем среднее арифметическое
        # sum(grades) - сумма всех оценок, len(grades) - количество оценок
        # Иначе возвращаем 0 (если оценок нет)
        return sum(self.grades)/len(self.grades) if self.grades else 0 
    
    # Метод для вывода информации о студенте
    def show_info(self):
        # Выводим имя студента
        print(f"Студент: {self.name}")
        # Выводим идентификатор студента
        print(f"ID: {self.student_id}")
        # Выводим список оценок
        print(f"Оценки: {self.grades}")
        # Выводим средний балл, форматированный до 1 знака после запятой
        print(f"Средний: {self.get_average():.1f}")


# Пример использования класса Student
# Создаем объект student класса Student с именем "Ольга Калашникова" и ID "9"
student = Student("Ольга Калашникова", "9")
# Добавляем оценку 5
student.add_grade(5)
# Добавляем оценку 6
student.add_grade(6)
# Добавляем оценку 8
student.add_grade(8)
# Выводим информацию о студенте
student.show_info()
